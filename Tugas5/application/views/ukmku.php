<h1>UKM K-Art</h1>
<hr>
<p>
  Visi : Mengumpulkan minat dan bakat<br>
  Misi : Mengasah kreativitas dan menghasilkan hasil karya yang kreatif<br></br>
  <u>Pengurus:</u>
  <ul>
    <li>Pembina: Husni Anggriani</li>
    <li>Ketua: Ilfan Diago</li>
  </ul>

  Ukm K-Art adalah ukm yang berhubungan dengan seni menggambar atau yang biasa disebut seni lukis.<br>
  Ukm ini berdiri tanggal 10 november 2016 yang diawali dengan rapat pemilihan ketua pada tanggal 15 november.<br>
  Pada rapat itu mahasiswa angkatan 2015 bernama ilfan diago dipilih sebagai ketua berdasarkan mayoritas pilihan anggota.<br><br>

  Pada rapat ke-2 dilaksanakan pada tanggal 21/11/16, dilakukan pemilihan nama dan kegiatan UKM ini ke depannya. <br>
  Setelah dilakukan voting oleh seluruh anggota UKM, K-Art ditetapkan sebagai nama dari UKM ini. Di rapat ini pun membahas <br>
  rencana untuk membuat sebuah proyek FlipBook sebanyak 50 lembar.<br><br>

  Pada rapat ke-3 dilakukan pengumpulan logo dari hasil karya anggota. Nantinya dari seluruh hasil yang ada, akan dipilih satu logo <br>
  terbaik yang akan digunakan untuk UKM ini.
</p>
