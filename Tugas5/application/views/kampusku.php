      STMIK Kharisma yang berada di Jl. Baji Ateka no. 20 provinsi sulawesi selatan, kota makassar adalah kampusku. <br>
      Kampus STMIK Kharisma berdiri pada tahun 2000. <br>
      Kampus ini dulunya ada di jalan kakatua, tetapi sekarang sudah pindah ke jl. Baji Ateka. <br>
