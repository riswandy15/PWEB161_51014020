      Kampus ini terbagi atas 2 program studi, yakni Sistem Informasi dan Teknik Informatika. <br>
      Kampus ini memiliki Lahan parkir yang dapat menampung kira-kira 15-16 mobil. <br>
      Jalan untuk dapat menuju kampus ini bisa dengan melewati jalan cendrawasih, kalau tidak tau silahkan cari di google. <br>
      Disin saya beritahu salah satu cara untuk menuju STMIK Kharisma : Pertama cari jalan Cendrawasih terlebih dahulu <br>
      Setelah jalan cendrawasih ketemu, kalau anda dari jalan haji bau silahkan cari SMA katholik cendrawasih. <br>
      Kalo sudah ketemu disebelah-sebelahnya pasti ada kampus UKI-Paulus, silahkan lurus terus. <br>
      Setelah anda terus jalan dan menemukan lampu LaLin, silahkan belok kiri. <br>
      Tak jauh dari sana disebelah kanan anda akan dapat menemukan kampus ini.
